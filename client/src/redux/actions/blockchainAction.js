export const blockchainAction =()=>async dispatch =>{
    dispatch({type:CONNECTION_REQUEST})
        if(window.ethereum){
            let web3 = new Web3(window.ethereum)
            try {
                const accounts = await window.ethereum.request({
                    method:"eth_accounts",
                });
				const contract_Token =new web3.eth.Contract(tokenABI,'0x883851e51EE4c58cB8cf89F829CB6a444D8f5205')
                const contract_MM =new web3.eth.Contract(abi,'0x705Df1D757d92Fc8D7D5B6f719Fa1988A0b9Dc5a')
                const provider = new Web3.providers.WebsocketProvider('wss://rinkeby.infura.io/ws/v3/745b5461920642a2bf4f3d4dacc09f0d')
                let web3_infura = new Web3(provider)
                const contract_Infura = new web3_infura.eth.Contract(abi,'0x705Df1D757d92Fc8D7D5B6f719Fa1988A0b9Dc5a')
                dispatch({
                    type:CONNECTION_SUCCESS,
                    payload:{
                        account:accounts[0],
                        contract_Infura:contract_Infura,
                        contract_MM:contract_MM,
						contract_Token:contract_Token,
                        web3:web3
                    }
                })
                if(!accounts.length ){
                    toast.warn('Login to Metamask')
                }
               
                ;
                window.ethereum.on('accountsChanged',accounts=>{
                    dispatch(updateAccount(accounts[0]))
                })
                window.ethereum.on("chainChanged", () => {
                    window.location.reload();
                });

            } catch (error) {
                dispatch({
                    type:CONNECTION_FAILED,
                    payload:"Something went wrong"
                })
                toast.warn('Something went wrong')
            }
        }else{
            dispatch({
                type:CONNECTION_FAILED,
                payload:"You have not installed Metamask"
            })
            toast.warn("You have not installed Metamask")
        }
        
    
}

export const updateAccount =(account)=>async dispatch=> {
	dispatch({
		type:UPDATE_ACCOUNT,
		payload:{
			account:account
	}})
}

